export function searchCountries(query: string): Country[] {
  if (!query) return countries.slice(0, 10);
  // Strip any leading emoji/flag characters for search (including regional indicators and tag sequences)
  const cleanQuery = query
    .replace(/^[\u{1F1E0}-\u{1F1FF}\u{1F3F4}\u{E0061}-\u{E007F}]+/u, "") // Regional indicators and tag sequences
    .replace(/^[\p{Emoji}\p{Emoji_Modifier}\p{Emoji_Component}\p{Emoji_Modifier_Base}\p{Emoji_Presentation}]+/u, "") // Other emojis
    .trim();
  if (!cleanQuery) return countries.slice(0, 10);
  const lowerQuery = cleanQuery.toLowerCase();
  return countries.filter((c) => c.name.toLowerCase().includes(lowerQuery));
}
